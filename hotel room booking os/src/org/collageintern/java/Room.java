package org.collageintern.java;

public class Room {

	public int roomNum;
	public String roomType;
	public String status;
	public int price;

	public Room(int roomNum, String roomType, String status, int price) {
		this.roomNum = roomNum;
		this.roomType = roomType;
		this.status = status;
		this.price = price;
	}
}
